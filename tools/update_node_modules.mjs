import { execSync } from 'child_process';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import fs from 'fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcDir = join(__dirname, '..');
const nodeModulesPath = join(srcDir, 'node_modules');
const depsFilePath = join(srcDir, 'DEPS');

if (!fs.existsSync(nodeModulesPath)) {
  console.error(`Error: node_modules directory not found at ${nodeModulesPath}`);
  console.error('Please run "npm install" before running this script.');
  process.exit(1);
}

console.log('Uploading node_modules to GCS... (This might take a while)');

// Passing the directory directly allows upload_to_google_storage_first_class.py 
// to automatically compress it to a tar.gz file and use its sha256sum as the object name.
const uploadCmd = `vpython3 third_party/depot_tools/upload_to_google_storage_first_class.py --bucket=chromium-bidi-node-modules node_modules`;

let output;
try {
  output = execSync(uploadCmd, { cwd: srcDir, encoding: 'utf-8' });
} catch (error) {
  console.error('Upload failed:');
  console.error(error.stdout);
  console.error(error.stderr);
  process.exit(1);
}

// Parse JSON from the python script's output
const jsonMatch = output.match(/\{[\s\S]*\}/);
if (!jsonMatch) {
  console.error('Could not find JSON output from upload script.');
  console.error('Output was:', output);
  process.exit(1);
}

const data = JSON.parse(jsonMatch[0]);
const obj = data.path.objects[0];

// Format: {object_name},{sha256sum},{size_bytes},{generation}
const objectInfo = `${obj.object_name},${obj.sha256sum},${obj.size_bytes},${obj.generation}`;

console.log('Updating DEPS file...');
const setdepCmd = `gclient setdep -r node_modules@"${objectInfo}" --deps-file="${depsFilePath}"`;
try {
  execSync(setdepCmd, { cwd: srcDir, stdio: 'inherit' });
} catch (error) {
  console.error('Updating DEPS failed.');
  process.exit(1);
}

console.log('Done!');
