import {execSync} from 'child_process';
import {join} from 'path';
import fs from 'fs';

const srcDir = join(import.meta.dirname, '..');
const nodeModulesPath = join(srcDir, 'node_modules');
const depsFilePath = join(srcDir, 'DEPS');

if (!fs.existsSync(nodeModulesPath)) {
  console.error(
    `Error: node_modules directory not found at ${nodeModulesPath}`,
  );
  console.error(
    'Please make sure node_modules is populated (e.g. via gclient sync) before running this script.',
  );
  process.exit(1);
}

const isForce = process.argv.includes('--force');

const excludeFilePath = join(import.meta.dirname, 'npm_exclude.txt');

console.log('Filtering node_modules...');
execSync(`rm -rf node_modules_filtered`, {cwd: srcDir});
execSync(
  `rsync -c --delete -rl -q --exclude-from="${excludeFilePath}" --prune-empty-dirs node_modules/ node_modules_filtered/`,
  {cwd: srcDir, stdio: 'inherit'},
);
execSync(`rm -rf node_modules`, {cwd: srcDir});
execSync(`mv node_modules_filtered node_modules`, {cwd: srcDir});

if (isForce) {
  console.log('Uploading node_modules to GCS... (This might take a while)');
} else {
  console.log('Dry run mode. Pass --force to actually upload.');
}

// Passing the directory directly allows upload_to_google_storage_first_class.py
// to automatically compress it to a tar.gz file and use its sha256sum as the object name.
let uploadCmd = `vpython3 ../third_party/depot_tools/upload_to_google_storage_first_class.py --bucket=chromium-nodejs --prefix=chromium-bidi .`;
if (!isForce) {
  uploadCmd += ' --dry-run';
}

let output;
try {
  output = execSync(uploadCmd, {cwd: nodeModulesPath, encoding: 'utf-8'});
  console.log(output);
} catch (error) {
  if (!isForce) {
    console.log(
      'Dry-run check failed. This usually means the node_modules archive does not exist on Google Storage yet.',
    );
    console.log('Pass --force to actually upload it and update DEPS.');
    process.exit(0);
  }
  console.error('Upload failed:');
  console.error(error.stdout);
  console.error(error.stderr);
  process.exit(1);
}

// Parse JSON from the python script's output
const jsonMatch = output.match(/\{[\s\S]*\}/);
if (!jsonMatch) {
  console.error('Could not find JSON output from upload script.');
  console.error('Output was:', output);
  process.exit(1);
}

const data = JSON.parse(jsonMatch[0]);
const obj = data.path.objects[0];

// Format: {object_name},{sha256sum},{size_bytes},{generation}
const objectInfo = `${obj.object_name},${obj.sha256sum},${obj.size_bytes},${obj.generation}`;

const setdepCmd = `gclient setdep -r node_modules@"${objectInfo}" --deps-file="${depsFilePath}"`;

if (isForce) {
  console.log('Updating DEPS file...');
  try {
    execSync(setdepCmd, {cwd: srcDir, stdio: 'inherit'});
  } catch {
    console.error('Updating DEPS failed.');
    process.exit(1);
  }
} else {
  console.log('Dry run: would have updated DEPS file using command:');
  console.log(setdepCmd);
}

console.log('Done!');
