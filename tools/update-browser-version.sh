set -e

# Change directory to the git repository root so the script works from any directory
cd "$(git rev-parse --show-toplevel)"

# Check requirements (always required as we always create PR)
if ! command -v gh &> /dev/null; then
  echo "Error: 'gh' CLI is required but not installed." >&2
  echo "Please install it first (e.g. 'brew install gh')." >&2
  exit 1
fi

if ! gh auth status &> /dev/null; then
  echo "Error: You are not authenticated with 'gh' CLI." >&2
  echo "Please run 'gh auth login' first." >&2
  exit 1
fi

CURRENT_BRANCH=$(git branch --show-current)
if [ -z "$CURRENT_BRANCH" ]; then
  echo "Error: Could not detect current git branch." >&2
  exit 1
fi

# Make sure there are no uncommitted changes
if [ -n "$(git status --porcelain)" ]; then
  echo "Error: You have uncommitted changes on branch '$CURRENT_BRANCH'." >&2
  echo "Please commit, stash, or discard them before running this script." >&2
  exit 1
fi


echo "Installing npm dependencies..."
npm ci

echo "Running browser update script..."
node tools/update_chrome_revision.mjs

COMMIT_MSG=""
if [ -f .browser_commit_msg ]; then
  COMMIT_MSG=$(cat .browser_commit_msg)
  rm -f .browser_commit_msg
fi

if [ -n "$COMMIT_MSG" ] && [ -n "$(git status --porcelain)" ]; then
  NEW_BRANCH="browser-automation-bot/update-browser-version-$(date +%s)"
  echo "Changes detected. Creating new branch '$NEW_BRANCH' based off '$CURRENT_BRANCH'..."
  git checkout -b "$NEW_BRANCH"

  echo "Committing changes: '$COMMIT_MSG'..."
  git add .browser package.json package-lock.json
  git commit -m "$COMMIT_MSG"

  echo "Pushing branch '$NEW_BRANCH' to origin..."
  git push origin "$NEW_BRANCH" --force

  PR_BODY=$(cat << 'EOF'
Automatically generated by tools/update-browser-version.sh

This requires some manual work:
1. Review and approve the PR.
2. Approve and run workflows.
3. Update WPT or e2e expectations and file issues if there was regression.
4. Merge the PR.
EOF
)
  echo "Creating Pull Request on GitHub..."
  gh pr create \
    --title "$COMMIT_MSG" \
    --body "$PR_BODY" \
    --head "$NEW_BRANCH" \
    --base "$CURRENT_BRANCH"

  echo "Restoring original branch '$CURRENT_BRANCH'..."
  git checkout "$CURRENT_BRANCH"
else
  echo "No updates required for the pinned browser version."
fi
